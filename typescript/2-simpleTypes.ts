/*
* Type Assignment - (assigning types to variables)
 When creating a variable, there are two main ways TypeScript assigns a type:

 * Explicit  - writing out the type

 * Implicit - forces TypeScript to "guess" the type (guessing the type means to "infer"), based on the assigned value: 

 * in both type assignments, another datatype CANNOT be reassigned to the same variable
 */

//  Explicit
let firstName: string = 'Dylan'

// Implicit
let lastName = 'Joe'
